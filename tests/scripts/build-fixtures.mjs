#!/usr/bin/env node
// Generates synthetic test fixtures (JPG, PNG, XLSX, EPUB) using only deps
// already present in the project. Run with `pnpm test:build-fixtures`.
//
// Inputs we keep as static files in the repo (they have permissive sources):
//   * pdf-image-only.pdf   — W3C dummy.pdf
//   * pdf-text-multipage.pdf — css4.pub USENIX example
//   * sample.docx          — Calibre demo
//
// Everything else is generated locally so we don't ship third-party artwork.

import { writeFileSync, statSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { createRequire } from 'node:module';
import { spawnSync } from 'node:child_process';

const require = createRequire(import.meta.url);
const here = dirname(fileURLToPath(import.meta.url));
const FIX = resolve(here, '../../test-fixtures');

console.log('Building synthetic fixtures into', FIX);

// ---------- sample.jpg: 320x200 solid block with diagonal stripes ----------
// Generate via macOS `sips` from a tiny seed PNG we create with pdf-lib's
// internal PNG encoder. Then sips converts to JPEG.

const { PDFDocument, rgb } = require(resolve(here, '../../node_modules/pdf-lib'));

async function buildSeedPdfWithRect() {
  const pdf = await PDFDocument.create();
  const w = 320;
  const h = 200;
  const page = pdf.addPage([w, h]);
  // Diagonal stripes for visual interest.
  for (let x = -h; x < w + h; x += 20) {
    page.drawRectangle({
      x,
      y: 0,
      width: 10,
      height: h,
      color: rgb(0.65, 0.35, 0.85),
      rotate: { type: 'degrees', angle: 45 },
    });
  }
  page.drawText('LocalConvert', { x: 80, y: 110, size: 24, color: rgb(1, 1, 1) });
  page.drawText('test fixture', { x: 90, y: 80, size: 16, color: rgb(1, 1, 1) });
  const bytes = await pdf.save();
  const seedPdf = resolve(FIX, 'sample.seed.pdf');
  writeFileSync(seedPdf, bytes);
  return seedPdf;
}

async function buildSampleJpg() {
  const seedPdf = await buildSeedPdfWithRect();
  // sips can rasterise PDF → JPEG on macOS without extra deps.
  const out = resolve(FIX, 'sample.jpg');
  const result = spawnSync(
    'sips',
    ['-s', 'format', 'jpeg', '-s', 'formatOptions', '90', seedPdf, '--out', out],
    { encoding: 'utf8' },
  );
  if (result.status !== 0) {
    throw new Error(`sips failed: ${result.stderr}`);
  }
  spawnSync('rm', [seedPdf]);
  return out;
}

// ---------- sample.xlsx: 3-sheet workbook with formulas and dates ----------
async function buildSampleXlsx() {
  const XLSX = require(resolve(here, '../../node_modules/xlsx'));
  const wb = XLSX.utils.book_new();

  const products = [
    ['Item', 'Quantity', 'Price', 'Total'],
    ['Espresso', 12, 2.5, { f: 'B2*C2' }],
    ['Cappuccino', 8, 3.5, { f: 'B3*C3' }],
    ['Latte', 5, 4.0, { f: 'B4*C4' }],
    ['Mocha', 3, 4.5, { f: 'B5*C5' }],
    ['', '', 'Total', { f: 'SUM(D2:D5)' }],
  ];
  const ws1 = XLSX.utils.aoa_to_sheet(products);
  XLSX.utils.book_append_sheet(wb, ws1, 'Sales');

  const monthly = [['Month', 'Revenue', 'Cost', 'Profit']];
  for (let m = 1; m <= 6; m++) {
    const rev = 1000 + m * 250;
    const cost = 400 + m * 50;
    monthly.push([`2026-0${m}`, rev, cost, rev - cost]);
  }
  const ws2 = XLSX.utils.aoa_to_sheet(monthly);
  XLSX.utils.book_append_sheet(wb, ws2, 'Monthly');

  const ws3 = XLSX.utils.aoa_to_sheet([
    ['LocalConvert XLSX fixture'],
    ['Purpose: round-trip XLSX → CSV / JSON / HTML / XLSX'],
    ['Generated by:', 'tests/scripts/build-fixtures.mjs'],
  ]);
  XLSX.utils.book_append_sheet(wb, ws3, 'Notes');

  const out = resolve(FIX, 'sample.xlsx');
  XLSX.writeFile(wb, out);
  return out;
}

// ---------- sample.epub: tiny 2-chapter EPUB with one inline SVG image ----------
async function buildSampleEpub() {
  const JSZip = require(resolve(here, '../../node_modules/jszip'));
  const zip = new JSZip();

  // EPUB structure
  zip.file('mimetype', 'application/epub+zip', { compression: 'STORE' });
  zip.file(
    'META-INF/container.xml',
    `<?xml version="1.0"?>
<container version="1.0" xmlns="urn:oasis:names:tc:opendocument:xmlns:container">
  <rootfiles>
    <rootfile full-path="OEBPS/content.opf" media-type="application/oebps-package+xml"/>
  </rootfiles>
</container>`,
  );
  zip.file(
    'OEBPS/content.opf',
    `<?xml version="1.0" encoding="UTF-8"?>
<package xmlns="http://www.idpf.org/2007/opf" version="3.0" unique-identifier="bookid">
  <metadata xmlns:dc="http://purl.org/dc/elements/1.1/">
    <dc:identifier id="bookid">localconvert-test-001</dc:identifier>
    <dc:title>LocalConvert Test Book</dc:title>
    <dc:creator>LocalConvert Test Suite</dc:creator>
    <dc:language>en</dc:language>
  </metadata>
  <manifest>
    <item id="chapter1" href="chapter1.xhtml" media-type="application/xhtml+xml"/>
    <item id="chapter2" href="chapter2.xhtml" media-type="application/xhtml+xml"/>
    <item id="cover" href="cover.svg" media-type="image/svg+xml"/>
  </manifest>
  <spine>
    <itemref idref="chapter1"/>
    <itemref idref="chapter2"/>
  </spine>
</package>`,
  );
  zip.file(
    'OEBPS/cover.svg',
    `<?xml version="1.0"?>
<svg xmlns="http://www.w3.org/2000/svg" width="200" height="100">
  <rect width="200" height="100" fill="#5a3aa0"/>
  <text x="100" y="55" fill="white" text-anchor="middle" font-family="sans-serif" font-size="18">LocalConvert</text>
</svg>`,
  );
  zip.file(
    'OEBPS/chapter1.xhtml',
    `<?xml version="1.0" encoding="UTF-8"?>
<!DOCTYPE html>
<html xmlns="http://www.w3.org/1999/xhtml">
<head><title>Chapter 1</title></head>
<body>
<h1>Chapter One: Introduction</h1>
<p>This is the first chapter of a synthetic EPUB used to exercise the
LocalConvert EPUB pipeline. It contains real prose so the
text-extraction path produces a useful word count above the image-only
heuristic threshold.</p>
<p>The book deliberately has multiple chapters so reordering and
chapter-walk logic are exercised end-to-end. A small SVG asset is
referenced below to confirm that asset rewriting works during
EPUB to HTML to PDF conversion.</p>
<p><img src="cover.svg" alt="cover"/></p>
</body>
</html>`,
  );
  zip.file(
    'OEBPS/chapter2.xhtml',
    `<?xml version="1.0" encoding="UTF-8"?>
<!DOCTYPE html>
<html xmlns="http://www.w3.org/1999/xhtml">
<head><title>Chapter 2</title></head>
<body>
<h1>Chapter Two: Round-Trips</h1>
<p>Round-trips matter. Converting an EPUB to HTML and then to PDF
should preserve chapter ordering, headings, paragraphs, and any
inlined or referenced imagery.</p>
<ul>
  <li>Chapter ordering: maintained by spine itemrefs.</li>
  <li>Image references: rewritten to data URIs at conversion time.</li>
  <li>Headings: h1/h2/h3 carry over to HTML output.</li>
</ul>
<blockquote>
  Privacy-first means no cloud APIs and no upload steps. Everything
  runs on-device.
</blockquote>
</body>
</html>`,
  );

  const bytes = await zip.generateAsync({
    type: 'nodebuffer',
    compression: 'DEFLATE',
    compressionOptions: { level: 6 },
  });
  const out = resolve(FIX, 'sample.epub');
  writeFileSync(out, bytes);
  return out;
}

// ---------- Run all builders ----------

const built = [];
built.push(await buildSampleJpg());
built.push(await buildSampleXlsx());
built.push(await buildSampleEpub());

console.log('\nGenerated fixtures:');
for (const path of built) {
  const size = statSync(path).size;
  console.log(`  ${path.replace(FIX + '/', '')}  ${size.toLocaleString()} bytes`);
}
